export function getProductBySlug(slug) {
  return ALL_PRODUCTS.find((p) => p.slug === slug) || null;
}

export function getRelatedProducts(product, count = 4) {
  return ALL_PRODUCTS
    .filter((p) => p.id !== product.id && p.category === product.category)
    .slice(0, count)
    .concat(
      ALL_PRODUCTS
        .filter((p) => p.id !== product.id && p.category !== product.category)
        .slice(0, Math.max(0, count - ALL_PRODUCTS.filter((p) => p.id !== product.id && p.category === product.category).length))
    )
    .slice(0, count);
}